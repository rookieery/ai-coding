export const t = (key: keyof typeof messages['zh-CN'], ...args: any[]) => {
  let msg = messages[currentLocale.value][key] || messages['zh-CN'][key];
  args.forEach((arg, i) => {
    msg = msg.replace(`{${i}}`, arg);
  });
  return msg;
};
